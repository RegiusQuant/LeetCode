import heapq
import random
from typing import List


class Solution:
    def kthSmallest(self, mat: List[List[int]], k: int) -> int:
        temp = mat[0]
        for r in mat[1:]:
            t = []
            for c in r:
                for x in temp:
                    t.append(x + c)
            temp = sorted(t)[:k]
        return temp[-1]


