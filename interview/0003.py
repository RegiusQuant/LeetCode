from typing import List


class Solution:
    def minAreaRect(self, points: List[List[int]]) -> int:
        s = set()
        for x, y in points:
            s.add((x, y))

        result = float('inf')
        for i in range(len(points)):
            for j in range(i + 1, len(points)):
                flag = True
                minx, maxx = min(points[i][0], points[j][0]), max(points[i][0], points[j][0])
                miny, maxy = min(points[i][1], points[j][1]), max(points[i][1], points[j][1])
                if minx == maxx or miny == maxy:
                    continue

                for x in [minx, maxx]:
                    for y in [miny, maxy]:
                        if (x, y) not in s:
                            flag = False
                            break
                if flag:
                    result = min(result, (maxx - minx) * (maxy - miny))
        return result if result != float('inf') else 0


